package day1;

public class DefaultValuesDataTypes {
	static byte BYTE;
	static short SHORT;
	static int INT;
	static long LONG;
	static float FLOAT;
	static double DOUBLE;
	static char CHAR; 
	static String STRING;
	public static void main(String[] args) {
		
		System.out.println("Default value of BYTE: " + BYTE);
		System.out.println("Default value of SHORT: " + SHORT);
		System.out.println("Default value of INT: " + INT);
		System.out.println("Default value of LONG: " + LONG);
		System.out.println("Default value of FLOAT: " + FLOAT);
		System.out.println("Default value of DOUBLE: " + DOUBLE);
		System.out.println("Default value of CHAR: " + CHAR);
		System.out.println("Default value of STRING: " + STRING);
		
	}
}